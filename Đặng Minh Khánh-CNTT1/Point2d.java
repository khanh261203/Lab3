package com.mycompany.lab3;

import java.awt.Point;

/**
 *
 * @author ADMIN
 */
public class Point2d {
    private float x=0.0f;
    private float y=0.0f ;
  
public Point2d()
{
}
public Point2d(float x, float y)
{
    this.x = x;
    this.y = y;
}

public float getx(float x){
     return x;
    
}
public float gety(float y){
     return y;
}

}

